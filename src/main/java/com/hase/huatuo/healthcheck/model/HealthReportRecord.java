package com.hase.huatuo.healthcheck.model;

public class HealthReportRecord {
	private String serailNumber;
	private String openId;
	private String reportDate;
	
	public String getSerailNumber() {
		return serailNumber;
	}
	public void setSerailNumber(String serailNumber) {
		this.serailNumber = serailNumber;
	}
	public String getOpenId() {
		return openId;
	}
	public void setOpenId(String openId) {
		this.openId = openId;
	}
	public String getReportDate() {
		return reportDate;
	}
	public void setReportDate(String reportDate) {
		this.reportDate = reportDate;
	}
}
